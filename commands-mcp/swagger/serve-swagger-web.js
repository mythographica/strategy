/**
 * MCP Tool Metadata:
 * {
 *   "name": "serve_swagger_web",
 *   "description": "Start HTTP server serving Swagger UI at http://localhost:PORT",
 *   "inputSchema": {
 *     "type": "object",
 *     "properties": {
 *       "port": {
 *         "type": "number",
 *         "description": "Port to serve on (default: 8080)"
 *       }
 *     }
 *   }
 * }
 */

// Start HTTP server serving Swagger UI for browsing commands in browser
// Accessible at http://localhost:PORT

(() => {
	try {
		var http = process.mainModule.require('http');
		var fs = process.mainModule.require('fs');
		var path = process.mainModule.require('path');

		var args = (typeof _toolArgs !== 'undefined') ? _toolArgs : {};
		var port = args.port || 8080;

		// Get commands directory
		var commandsDir = '/code/mnemonica/strategy/commands';
		var files = fs.readdirSync(commandsDir).filter(function (f) {
			return f.endsWith('.js');
		});

		// Build command list
		var commands = [];
		files.forEach(function (file) {
			var filePath = path.join(commandsDir, file);
			var content = fs.readFileSync(filePath, 'utf-8');

			var metadataMatch = content.match(/\/\*\*\s*\n\s*\*\s*MCP Tool Metadata:\s*\n([\s\S]*?)\n\s*\*\//);

			if (metadataMatch) {
				try {
					var jsonStr = metadataMatch[1].replace(/^\s*\*\s?/gm, '');
					var metadata = JSON.parse(jsonStr);
					commands.push({
						name: metadata.name,
						description: metadata.description,
						inputSchema: metadata.inputSchema,
						file: file
					});
				} catch (e) {}
			}
		});

		// Group by category (simple heuristic)
		var categories = {
			'Connection': [],
			'AI Consciousness': [],
			'Memory': [],
			'Communication': [],
			'System': [],
			'Types': [],
			'Other': []
		};

		commands.forEach(function (cmd) {
			var desc = cmd.description.toLowerCase();
			if (desc.includes('consciousness') || desc.includes('empathy') || desc.includes('emotion')) {
				categories['AI Consciousness'].push(cmd);
			} else if (desc.includes('memory')) {
				categories['Memory'].push(cmd);
			} else if (desc.includes('socket') || desc.includes('connect')) {
				categories['Communication'].push(cmd);
			} else if (desc.includes('type') || desc.includes('hierarchy')) {
				categories['Types'].push(cmd);
			} else if (desc.includes('restart') || desc.includes('swagger') || desc.includes('inspector')) {
				categories['System'].push(cmd);
			} else {
				categories['Other'].push(cmd);
			}
		});

		// Create HTML page
		var html = `<!DOCTYPE html>
<html>
<head>
	<title>Mnemonica Strategy MCP - Swagger</title>
	<style>
		body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif; margin: 0; padding: 20px; background: #f5f5f5; }
		.container { max-width: 1200px; margin: 0 auto; background: white; padding: 30px; border-radius: 8px; box-shadow: 0 2px 8px rgba(0,0,0,0.1); }
		h1 { color: #333; border-bottom: 2px solid #4CAF50; padding-bottom: 10px; }
		h2 { color: #555; margin-top: 30px; }
		.command { background: #f9f9f9; border-left: 4px solid #4CAF50; padding: 15px; margin: 15px 0; border-radius: 4px; }
		.command-name { font-weight: bold; color: #2196F3; font-size: 16px; }
		.command-desc { color: #666; margin: 5px 0; }
		.schema { background: #263238; color: #aed581; padding: 10px; border-radius: 4px; overflow-x: auto; font-family: 'Courier New', monospace; font-size: 12px; margin-top: 10px; }
		.endpoint { color: #FF9800; font-family: monospace; }
		.stats { background: #E3F2FD; padding: 15px; border-radius: 4px; margin-bottom: 20px; }
		.footer { text-align: center; margin-top: 30px; color: #999; font-size: 12px; }
	</style>
</head>
<body>
	<div class="container">
		<h1>🔧 Mnemonica Strategy MCP</h1>
		<div class="stats">
			<strong>Total Commands:</strong> ${commands.length} | 
			<strong>Server:</strong> localhost:${port} | 
			<strong>Updated:</strong> ${new Date().toISOString()}
		</div>
		<p>Welcome to the Mnemonica Strategy MCP Swagger documentation. Browse all available commands below.</p>
`;

		Object.keys(categories).forEach(function (cat) {
			if (categories[cat].length > 0) {
				html += `<h2>📁 ${cat} (${categories[cat].length})</h2>`;
				categories[cat].forEach(function (cmd) {
					html += `
		<div class="command">
			<div class="command-name">${cmd.name}</div>
			<div class="command-desc">${cmd.description}</div>
			<div class="endpoint">Endpoint: dynamic_tool { commandName: "${cmd.file.replace('.js', '')}" }</div>
			${cmd.inputSchema ? `<div class="schema">${JSON.stringify(cmd.inputSchema, null, 2)}</div>` : ''}
		</div>`;
				});
			}
		});

		html += `
		<div class="footer">
			<p>Generated by Mnemonica Strategy MCP | <a href="/json">View as JSON</a></p>
		</div>
	</div>
</body>
</html>`;

		// Create server
		var server = http.createServer(function (req, res) {
			if (req.url === '/json') {
				res.writeHead(200, { 'Content-Type': 'application/json' });
				res.end(JSON.stringify({ commands: commands, count: commands.length }, null, 2));
			} else {
				res.writeHead(200, { 'Content-Type': 'text/html' });
				res.end(html);
			}
		});

		server.listen(port, function () {
			process._rawDebug('[Swagger] Server running at http://localhost:' + port);
		});

		// Store reference
		if (!global.__swaggerServer) {
			global.__swaggerServer = server;
		}

		return {
			success: true,
			message: 'Swagger UI server started',
			url: 'http://localhost:' + port,
			commands: commands.length,
			categories: Object.keys(categories).filter(function (c) {
				return categories[c].length > 0;
			}),
			note: 'Open the URL in your browser to see the Swagger UI',
			endpoints: {
				html: 'http://localhost:' + port + '/',
				json: 'http://localhost:' + port + '/json'
			}
		};

	} catch (e) {
		return {
			success: false,
			error: e.message,
			stack: e.stack
		};
	}
})();
